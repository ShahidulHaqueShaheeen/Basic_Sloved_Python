    for i in range(1, node+1):
        if in_cnt[i] == 0:
            pq.put(i)
    count = 0
    while not pq.empty():
        frnt = pq.get()
        order.append(frnt)
        count += 1

        for v in adj_list[frnt]:
            in_cnt[v] -= 1
            if in_cnt[v] == 0:
                pq.put(v)
    return count


